
public class AssignInfo {
	private String assignResult, assignMessage;
	private Course course;

	public AssignInfo() {

	}

	public String getAssignResult() {
		return assignResult;
	}

	public String getAssignMessage() {
		return assignMessage;
	}

	public Course getCourse() {
		return course;
	}

	public String setAssignResult(String assignResult) {
		return this.assignResult = assignResult;
	}

	public String setAssignMessage(String assignMessage) {
		return this.assignMessage = assignMessage;
	}

	public Course setCourse(Course course) {
		return this.course = course;
	}
}
